def _raw_os_locale() -> str:
    """OS が設定している言語（ロケール）の生文字列を取り出す。

    例: "ja_JP" / "ja-JP" / "zh-Hant-TW" / "en_US" など。
    取れなければ空文字を返す。
    """
    # Windows はユーザーのUI言語名を直接取得できる（"ja-JP" 等）
    try:
        import ctypes
        buf = ctypes.create_unicode_buffer(85)
        if ctypes.windll.kernel32.GetUserDefaultLocaleName(buf, 85):
            if buf.value:
                return buf.value
    except Exception:
        pass
    # その他のOS（mac / Linux）や、上で取れなかったとき
    try:
        loc = locale.getdefaultlocale()[0]
        if loc:
            return loc
    except Exception:
        pass
    for env in ("LC_ALL", "LC_MESSAGES", "LANG", "LANGUAGE"):
        v = os.environ.get(env)
        if v:
            return v
    return ""


def detect_os_lang() -> str:
    """OS の言語に合うサポート言語コードを返す。

    サポートしていない言語のときは DEFAULT_LANG（英語）にフォールバックする。
    中国語は地域・字体から簡体字(zh-CN)／繁体字(zh-TW)を見分ける。
    """
    raw = _raw_os_locale().replace("-", "_").lower()
    parts = [p for p in raw.split("_") if p]
    if not parts:
        return DEFAULT_LANG

    primary = parts[0]
    if primary == "zh":
        rest = set(parts[1:])
        # 繁体字を使う地域・字体なら zh-TW、それ以外は zh-CN
        if rest & {"tw", "hk", "mo", "hant"}:
            return "zh-TW"
        return "zh-CN"
    if primary in translations:
        return primary
    return DEFAULT_LANG


def t(lang: str, key: str, **params) -> str:
    """指定言語の訳文を返す。

    その言語に無いキーは「日本語 → キー名」の順でフォールバックする。
    {name} のような差し込みは params で渡す。
    """
    table = translations.get(lang, translations[DEFAULT_LANG])
    text = table.get(key)
    if text is None:
        text = translations[DEFAULT_LANG].get(key, key)
    if params:
        try:
            return text.format(**params)
        except Exception:
            return text
    return text
